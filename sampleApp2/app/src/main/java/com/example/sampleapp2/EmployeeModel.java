package com.example.sampleapp2;

public class EmployeeModel {

    public String id, name, age, email, password;

    public EmployeeModel(String id, String name, String age, String email, String password){
        this.id = id;
        this.name = name;
        this.age = age;
        this.email = email;
        this.password = password;
    }
}
